int main(int argc, char **argv)
{
	int		a;

	a = 0;
	if (argc <= 1)
		return (0);
	while (a <= argc - 1)
	{
		while (*argv[a] != 0)
		{
			ft_putchar(*argv[a]);
			argv[a]++;
		}
		a++;
		ft_putchar('\n');
	}
	return (1);
}
