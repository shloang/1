int		ft_count_if(char **tab, int (*f)(char*))
{
	int		x;
	int		n;

	x = 0;
	n = 0;
	while (*(tab + x))
	{
		if (f(*(tab + x)) == 1)
			n++;
		x++;
	}
	return (n);
}
